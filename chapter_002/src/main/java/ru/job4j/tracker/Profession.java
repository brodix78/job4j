package ru.job4j.tracker;

public class Profession {
    public String name;
    public String company;

    public Profession() {
    }

    public void inputName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setCompany(String name) {
        this.name = name;
    }

    public String getCompany() {
        return this.name;
    }
}